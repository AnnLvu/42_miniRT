#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*ptr;
	size_t	i;

	ptr = (char *)s;
	i = 0;
	while (i < n)
	{
		if (*((unsigned char *)ptr + i) == (unsigned char)c)
			return (ptr + i);
		i++;
	}
	return (NULL);
}
